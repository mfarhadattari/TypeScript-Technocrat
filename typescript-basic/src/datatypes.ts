{
  // Implicit Data Type
  let myName = "Mohammad Farhad";
  let age = 19;
  let isMarried = false;
  let a = undefined;
  let b = null;
  console.log(myName, age, isMarried, a, b);

  // Explicit Data Type
  let name2: string = "Mohammad Farhad";
  let age2: number = 50;
  let isAdult: boolean = true;
  let u: undefined = undefined;
  let n: null = null;
  console.log(name2, age2, isAdult, u, n);
}
