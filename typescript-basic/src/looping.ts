{
  // ----------->> for loop ---------->>
  for (let i = 1; i <= 10; i++) {
    console.log(i);
  }

  // ----------->> while loop ---------->>
  let i = 10;
  while (i >= 0) {
    console.log(i);
    i--;
  }

  // ---------------->> Do While Loop ------------>>
  let n = 100;
  do {
    console.log(n);
    n += 100;
  } while (n <= 1000);
}
